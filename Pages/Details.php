<h1>Details about COVID-19</h1>

<h3>
    An Overview
</h3>
<p class="info">
    The <strong>COVID-19 pandemic</strong>, also known as the <em>coronavirus pandemic</em>, is an ongoing pandemic of coronavirus disease 2019 (COVID‑19) caused by severe acute respiratory syndrome coronavirus 2 (SARS‑CoV‑2). The outbreak was first identified in <em>Wuhan, China</em>, in early December 2019. The World Health Organization declared the outbreak a <strong>Public Health Emergency of International Concern</strong> on 30 January, and a <strong>pandemic</strong> on 11 March. <br><br>
    As of 10 May 2020, more than <strong>4.09 million cases of COVID-19</strong> have been reported in over 187 countries and territories, resulting in more than 282,000 deaths. More than 1.4 million people have recovered.
</p>
<h3>
    How is it Spread?
</h3>
<p class="info">
    The virus is primarily spread between people during <strong>close contact</strong>, most often via small droplets produced by <strong>coughing, sneezing, and talking</strong>. The droplets usually fall to the ground or onto surfaces rather than travelling through air over long distances. Less commonly, people may become infected by <strong>touching</strong> a contaminated surface and then touching their face. It is most contagious during the first three days after the onset of symptoms, although spread may be possible before symptoms appear, or from people who do not show symptoms.
</p>
<h3>
    What preventive measures can we take?
</h3>
<p class="info">
    Recommended preventive measures include <strong>hand washing, covering one's mouth when coughing, maintaining distance from other people, wearing a face mask in public settings, and monitoring and self-isolation for people who suspect they are infected</strong>. Authorities worldwide have responded by implementing <strong>travel restrictions, lockdowns, workplace hazard controls, and facility closures</strong>. Many places have also worked to <strong>increase testing capacity and trace contacts of infected persons</strong>.
</p>
<h3>
    Impacts of COVID-19
</h3>
<p class="info">
    The pandemic has caused <strong>severe global socioeconomic disruption</strong>, including the largest global recession since the Great Depression. It has led to the <strong>postponement or cancellation of sporting, religious, political and cultural events</strong>, <strong>widespread supply shortages</strong> exacerbated by panic buying, and <strong>decreased emissions of pollutants and greenhouse gases</strong>. Schools, universities, and colleges have closed either on a nationwide or local basis in 190 countries, affecting approximately 73.5 per cent of the world's student population. <strong>Misinformation</strong> about the virus has spread online, and there have been incidents of <strong>xenophobia and discrimination</strong> against Chinese people and against those perceived as being Chinese, or as being from areas with high infection rates.
</p>
<h3>
    A Brief Timeline
</h3>
<p class="info">
    <strong>1 December 2019</strong>    : The earliest known person with symptoms of COVID-19 was discovered to have fallen ill.
    <strong>31 December 2019</strong>   : Health authorities in China reported to the World Health Organisation (WHO) a cluster of viral pneumonia cases of unknown cause in Wuhan, Hubei Province, and an investigation was launched in <strong>early January 2020</strong>.<br/>
    <strong>30 January 2020</strong>    : The WHO declared the outbreak a Public Health Emergency of International Concern (PHEIC); at that date there were 7,818 cases confirmed globally, affecting 19 countries in five WHO regions. The early cases mostly had links to the Huanan Seafood Wholesale Market and so the virus is thought to have a zoonotic origin. The virus that caused the outbreak is known as SARS‑CoV‑2, a newly discovered virus closely related to bat coronaviruses, pangolin coronaviruses, and SARS-CoV. The scientific consensus is that COVID-19 has a natural origin. <br/>
    <strong>11 March 2020</strong>      : The WHO recognized the spread of COVID-19 as a pandemic. Europe, Iran, the U.S., South Korea, and Japan reported surging cases and their total numbers quickly passed China's.
    <strong>13 March 2020</strong>      : An unverified report from the South China Morning Post suggested a case traced back to 17 November 2019 (a 55-year-old from Hubei) may have been the first person infected.
</p>

