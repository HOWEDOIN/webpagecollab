<h1>Introduction to COVID-19</h1>

<p>
    <strong>Coronavirus disease 2019 (COVID-19)</strong> is an infectious disease caused by severe acute respiratory syndrome coronavirus 2 (SARS-CoV-2).
</p>
<p>
    It was first identified in December 2019 in Wuhan, China, and has since spread globally, resulting in an ongoing pandemic.
</p>
<p>
    As of 10 May 2020, more than <strong>4.09 million cases</strong> have been reported across <strong>187 countries and territories</strong>, resulting in more than <strong>282,000 deaths</strong>.
</p>
<p>
    More than <strong>1.4 million people</strong> have recovered.
</p>
